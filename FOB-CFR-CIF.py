print(f'国外运费的单价 = {国外运费} / {数量} = {国外运费的单价}')

print(f'CFR = {FOB} + {国外运费的单价} = {CFR}')

print(f'CIF = {CFR} / (1 - (1 + {投保加成率}) * {保险费率}) = {CIF}')
