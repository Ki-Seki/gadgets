print(f'出口总成本 = {实际采购成本} + {国内费用} = {出口总成本}')
print(f'出口成本价格 = {出口总成本} / {数量} = {出口成本价格}')

print(f'出口成本折价美元 = {出口成本价格} / {美元兑人民币汇率} = {出口成本折价美元}')
print(f'FOB = {出口成本折价美元} * (1 + {利润率}) = {FOB}')

